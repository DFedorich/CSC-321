package P1;

public class A {
	private int x;
	protected int y;
	public int z;
	public A()
	{
		System.out.println("Hi From Class A");
	}
	public void set_x(int x_)
	{
		x=x_;
	}
	public int get_x()
	{
		return x;
	}
	public void set_y(int y_)
	{
		y=y_;
	}
	public int get_y()
	{
		return y;
	}
	public void set_z(int z_)
	{
		z=z_;
	}
	public int get_z()
	{
		return z;
	}
}
